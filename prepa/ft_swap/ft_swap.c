#include <stdio.h>
#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int l;

	l = *a;
	*a = *b;
	*b = l;
}
int main ()
{
	int x;
	int y;

	x = 6;
	y = 2;
	ft_swap(&x, &y);
	printf("%d %d", x, y);
}
